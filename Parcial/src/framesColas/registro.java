    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        JOptionPane.showMessageDialog(this, "Costo De Atencion Denuncia 14.000");
        
        String nombre;
        String cc;
        do
        {
            nombre = JOptionPane.showInputDialog("Digite Nombre: ");
            cc = JOptionPane.showInputDialog("Digite Número de Documento: ");
            if(nombre.isEmpty() || cc.isEmpty())
            {
                JOptionPane.showMessageDialog(this, "Llene los datos requeridos", "ERROR AL AGREGAR", JOptionPane.ERROR_MESSAGE);
            }
        }
        while(nombre.isEmpty() || cc.isEmpty());
        
        cont++;
        t.getColaPQR().insertNodo(cont);
        turnos.add(new Turn(nombre, cc));
        JOptionPane.showMessageDialog(this, "Su turno es el: E"+cont);
        
        t.setContPQR(cont);
        
        t.jLabel7.setText(String.valueOf(t.getColaPQR().getTamanio()));
        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(t.getColaPQR().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "No hay denuncias de éste tipo agregadas","ERROR EN EJECUCIÓN", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            Nodo temp = t.getColaPQR().getRaiz();
            Turn tn = turnos.get(0);
            JOptionPane.showMessageDialog(this, "Turno: E"+temp.getData()+"\n"
                + "Nombre: "+tn.getNombre()+"\n"
                + "N° Doc: "+tn.getCc());
            
            String calificacion;
            do
            {
                calificacion = JOptionPane.showInputDialog("Calificacion de atencion (1-5): ");
                
                if(calificacion.isEmpty())
                {
                    JOptionPane.showMessageDialog(this, "Debe colocar una calificación", "ERROR AL CALIFICAR", JOptionPane.ERROR_MESSAGE);
                }
            }
            while(calificacion.isEmpty());
            
            ta.add(new TurnoAtendido("E"+temp.getData(), tn.getNombre(), tn.getCc(), calificacion));
            
            t.getColaPQR().eliminarNodo(temp.getData());
            turnos.remove(tn);
        }
        
        t.jLabel7.setText(String.valueOf(t.getColaPQR().getTamanio()));
    }//GEN-LAST:event_jButton3ActionPerformed

