    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        p = new Pila();
        String txt = txtOp.getText();
        int size = txt.length();
        boolean signos = true;
        boolean caracter = true;
        for(int i = 0; i<size; i++)
        {
            String temp = txt.substring(i, i+1);
            if(p.esSoloMatematica(temp))
            {
                if(!p.signosIguales(temp))
                {
                    p.insertarNodo(temp);//a-z
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "Error en escritura de signos", "error", JOptionPane.ERROR_MESSAGE);
                    signos = false;
                }
            }
            else
            {
                caracter = false;
            }
        }
        
        if(p.todoCorrecto(this) && signos && caracter)
        {
            JOptionPane.showMessageDialog(this, "VerificaciÃ³n Realizada\n"
                    + "Escrito correctamente");
            txtOp.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

